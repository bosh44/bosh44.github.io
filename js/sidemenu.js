function updates() {
  $('.updates').css({'display':'block'});
  $('.about').css({'display':'none'});
  $('.blog').css({'display':'none'});
}

function about() {
  $('.updates').css({'display':'none'});
  $('.about').css({'display':'block'});
  $('.blog').css({'display':'none'});
}

function blog() {
  $('.updates').css({'display':'none'});
  $('.about').css({'display':'none'});
  $('.blog').css({'display':'block'});
}
